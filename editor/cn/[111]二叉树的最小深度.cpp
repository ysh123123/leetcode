class Solution {
public:
    int minDepth(TreeNode* root) {
        std::queue<TreeNode*> tmp;
        if(root!=nullptr) tmp.push(root);
        else return 0;
        int deep = 1;
        while (!tmp.empty())
        {
            int size = tmp.size();
            while (size--)
            {
                auto cur = tmp.front();
                tmp.pop();
                if(cur->left==nullptr&&cur->right== nullptr) return deep;
                if(cur->left!=nullptr) tmp.push(cur->left);
                if(cur->right!=nullptr) tmp.push(cur->right);
            }
            deep++;
        }
        return deep;
    }
};
